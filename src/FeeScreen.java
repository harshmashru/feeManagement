    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            conne = DriverManager.getConnection("jdbc:derby://localhost:1527/feeinfo","fee","fee");
            sta = (Statement) conne.createStatement();
            String query = "insert into fee(fname,std,addr,mno,eadd)values(?,?,?,?,?)";
            PreparedStatement pst = conne.prepareStatement(query);
            pst.setString(1, fname.getText());
            pst.setString(2, std.getText());
            pst.setString(3, addr.getText());
            pst.setString(4, mno.getText());
            pst.setString(5, eadd.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Details Submitted !");
            }
        catch(SQLException ex){
                Logger.getLogger(FeeManagementApp.class.getName()).log(Level.SEVERE,null,ex);
            }
        PayScreen ps = new PayScreen();
        ps.setSize(500, 300);
        ps.setVisible(true);
        ps.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
    }//GEN-LAST:event_jButton1ActionPerformed

